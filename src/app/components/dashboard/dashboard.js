let getData = () => {

const data = {
  labels: Object.keys(localStorage),
  datasets: [{
    label: 'Votes by Restaurant',
    data: Object.values(localStorage),
    backgroundColor: [
      'rgba(255, 99, 132, 0.2)',
      'rgba(255, 159, 64, 0.2)',
      'rgba(255, 205, 86, 0.2)',
      'rgba(75, 192, 192, 0.2)',
      'rgba(54, 162, 235, 0.2)',
      'rgba(153, 102, 255, 0.2)',
      'rgba(201, 203, 207, 0.2)'
    ],
    borderColor: [
      'rgb(255, 99, 132)',
      'rgb(255, 159, 64)',
      'rgb(255, 205, 86)',
      'rgb(75, 192, 192)',
      'rgb(54, 162, 235)',
      'rgb(153, 102, 255)',
      'rgb(201, 203, 207)'
    ],
    borderWidth: 1
  }]
};
return data;
}

let getonfig = (data) => {
const chartConfig = {
  type: 'bar',
  data: data,
  options: {
    scales: {
      y: {
        beginAtZero: true
      }
    }
  },
};
return chartConfig;
}

function Dashboard() {
  const chartContainer = React.useRef();
  const [chartInstance, setChartInstance] = React.useState();

  useEffect(() => {
    if (chartContainer && chartContainer.current) {
      let data = getData();
      console.log(data)
      const newChartInstance = new Chart(chartContainer.current, getonfig(data));
      setChartInstance(newChartInstance);
    }
   
  }, [chartContainer]);

    return (
      <div className="Dashboard">
        <canvas ref={chartContainer} />
      </div>
    );
  }
  
  export default Dashboard;